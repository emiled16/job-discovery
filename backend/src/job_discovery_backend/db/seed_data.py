_REGION_LABELS = {
    "quebec": "Quebec, Canada",
    "canada": "Canada",
    "us": "United States",
    "dubai": "Dubai, UAE",
}
_ACTIVE_SOURCE_TYPES = {"applytojob", "ashby", "greenhouse", "lever", "manual", "smartrecruiters", "workday"}
_NON_ALNUM_PATTERN = re.compile(r"[^a-z0-9]+")
_MULTI_DASH_PATTERN = re.compile(r"-{2,}")


def _seed_uuid(kind: str, slug: str, qualifier: str = "") -> str:
    return str(uuid5(NAMESPACE_URL, f"job-discovery:{kind}:{slug}:{qualifier}"))


def _slugify(value: str) -> str:
    normalized = _NON_ALNUM_PATTERN.sub("-", value.lower()).strip("-")
    return _MULTI_DASH_PATTERN.sub("-", normalized)


def _default_base_url(source_type: str, external_key: str, website_url: str | None) -> str | None:
    if source_type == "applytojob":
        return f"https://{external_key}.applytojob.com/apply"
    if source_type == "ashby":
        return f"https://jobs.ashbyhq.com/{external_key}"
    if source_type == "greenhouse":
        return f"https://boards.greenhouse.io/{external_key}"
    if source_type == "lever":
        return f"https://jobs.lever.co/{external_key}"
    return website_url


def _build_seed_record(
    *,
    region: str,
    name: str,
    slug: str | None = None,
    source_type: str = "manual",
    external_key: str | None = None,
    website_url: str | None = None,
    base_url: str | None = None,
    configuration: dict | None = None,
) -> dict:
    company_slug = slug or _slugify(name)
    source_key = external_key or company_slug
    is_active = source_type in _ACTIVE_SOURCE_TYPES
    lifecycle_status = "active" if is_active else "paused"
    resolved_base_url = base_url or _default_base_url(source_type, source_key, website_url)
    sync_tier = (
        "sync-ready"
        if source_type in {"applytojob", "ashby", "greenhouse", "lever", "smartrecruiters", "workday"}
        else "operator-managed"
    )
    return {
        "company": {
            "id": _seed_uuid("company", company_slug),
            "slug": company_slug,
            "name": name,
            "website_url": website_url,
            "description": f"Seeded {sync_tier} company for { _REGION_LABELS[region] }.",
            "lifecycle_status": lifecycle_status,
        },
        "source": {
            "id": _seed_uuid("source", company_slug, source_type),
            "source_type": source_type,
            "external_key": source_key,
            "base_url": resolved_base_url,
            "configuration": {
                "seed_region": region,
                "seed_region_label": _REGION_LABELS[region],
                "seed_sync_tier": sync_tier,
                **(configuration or {}),
            },
            "is_enabled": is_active,
        },
    }


