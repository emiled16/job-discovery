def _catalog_companies(region: str, names: tuple[str, ...]) -> tuple[dict, ...]:
    return tuple(_build_seed_record(region=region, name=name) for name in names)


def _dedupe_records(*record_groups: tuple[dict, ...]) -> tuple[dict, ...]:
    seen_slugs: set[str] = set()
    records: list[dict] = []
    for group in record_groups:
        for record in group:
            slug = record["company"]["slug"]
            if slug in seen_slugs:
                continue
            seen_slugs.add(slug)
            records.append(record)
    return tuple(records)


